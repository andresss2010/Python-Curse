#suma y resta ( + y - )
suma = 5 + 3
resta = 5 - 3

#multiplicacion y division ( * y / )
multiplicacion = 5 * 3
division = 5 / 3

#potenciacion ( ** )
potencia = 5 ** 3

#division baja ( // )
division_baja = 5 // 3

#resto o modulo ( % )
resto = 5 % 3

#type() nos devuelve el tipo de dato
tipo_de_dato = type(resto)

print("suma:", suma)
print("resta:", resta)
print("multiplicacion:", multiplicacion)
print("division:", division)
print("potencia:", potencia)
print("division baja:", division_baja)
print("resto:", resto)
print("tipo de dato:", tipo_de_dato)